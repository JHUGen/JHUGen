      include 'masses.f'
      real(dp):: X12,X13,X14,X23,X24,N12,N35,N45,M,MH,RES
      N12 = 1/X12
      N35 = 1/(X12+X14+X24)
      N45 = 1/(X12+X13+X23)
      M = mt
      MH = hmass

