      subroutine qqb_tottth_old(p,msq)
      implicit none
      include 'types.f'
      
************************************************************************
*     Author: R.K. Ellis                                               *
*     December, 1999.                                                  *
*     calculate the element squared 
*     for the process                                                  *
c----My notation                                                       *
C     q(-p1) +qbar(-p2)=t(p3)+t(p4)+h(p5)
C  
************************************************************************
      include 'constants.f'
      include 'nf.f'
      include 'mxpart.f'
      include 'cplx.h'
      include 'ewcouple.f'
      include 'qcdcouple.f'
      include 'couple.f'
      include 'kpart.f'
      include 'masses.f'
      include 'msbarmasses.f'
      include 'first.f'
      
      integer:: j,k
      real(dp):: msq(-nf:nf,-nf:nf),p(mxpart,4)
      real(dp):: wtqqb,wtgg,massfrun,mt_eff
      real(dp):: dot,DHQQ,DHGG,facqq,facgg,X12,X13,X14,X23,X24
      save mt_eff
!$omp threadprivate(mt_eff) 
      if (first) then
c--- run mt to appropriate scale
        if (kpart==klord) then
          mt_eff=massfrun(mt_msbar,hmass,amz,1)
        else
          mt_eff=massfrun(mt_msbar,hmass,amz,2)
        endif
        first=.false.
      endif





      x12=+2d0*dot(p,1,2)
      x13=+2d0*dot(p,1,3)
      x14=+2d0*dot(p,1,4)
      x23=+2d0*dot(p,2,3)
      x24=+2d0*dot(p,2,4)

      wtqqb=DHQQ(X12,X13,X14,X23,X24)
      wtgg=DHGG(X12,X13,X14,X23,X24)
      facqq=V/4d0*gsq**2*gwsq*mt_eff**2/wmass**2/4d0
      facgg=facqq*4d0/3d0
C----set all elements to zero
      do j=-nf,nf
      do k=-nf,nf
      msq(j,k)=0d0
      enddo
      enddo

C---fill qb-q, gg and q-qb elements
      do j=-nf,nf
      if (j < 0) then
          msq(j,-j)=aveqq*facqq*wtqqb
      elseif (j == 0) then
          msq(j,j)=avegg*facgg*wtgg
      elseif (j > 0) then
          msq(j,-j)=aveqq*facqq*wtqqb
      endif
      enddo
      return
      end
 
